package oot.lab6.group2.no2;

public class Team {

    private String name;
    private String manager;
    
    public Team(String name, String manager) {
        this.name = name;
        this.manager = manager;
    }

    public String getName() {
        return name;
    }

    public String getManager() {
        return manager;
    }
           
}
